package com.loiane.cursojava.aula11;

public class VariavelBoolean {

    public static void main(String[] args) {
        boolean verdadeiro = true;

        boolean falso = false;

        System.out.print("O valor de verdadeiro é: " + verdadeiro);
        System.out.println("");
        System.out.println("--------");

        System.out.print("O valor de falso é: " + falso);
    }
}
